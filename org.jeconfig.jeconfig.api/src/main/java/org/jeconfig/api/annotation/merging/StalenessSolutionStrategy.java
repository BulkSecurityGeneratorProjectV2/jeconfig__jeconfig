package org.jeconfig.api.annotation.merging;

/**
 * Specifies how stale configuration properties should be handled.<br>
 * A configuration property is stale when the parent property on which the property bases on has been changed.
 */
public enum StalenessSolutionStrategy {

	/** Determines that the stale changes in the child configuration should be discarded and the parent should be used. */
	USE_PARENT,

	/** Determines that the parent and child configurations should be merged using the annotated merging strategy. */
	MERGE
}
